#include<bits/stdc++.h>
using namespace std;

int main()
{
    int i;

    for(i=1;i<8;i++)
    {
       switch(i)
    {
       case 1 :
       { 
           cout<<"\nSUNDAY";
           break;
       }
       case 2 :
       {
           cout<<"\nMONDAY";
           break;
       } 
       case 3 :
       {
           cout<<"\nTUESDAY";
           break;
       } 
       case 4 :
       {
           cout<<"\nWEDNESDAY";
           break;
       } 
       case 5 :
       {
           cout<<"\nTHURSDAY";
           break;
       } 
       case 6 :
       {
           cout<<"\nFRIDAY";
           break;
       } 
       case 7 :
       {
           cout<<"\nSATURDAY\n";
           break;
       } 
       default :
       {
         cout<<"\n";
           break;
       } 
    }
    }
    return 0;
}